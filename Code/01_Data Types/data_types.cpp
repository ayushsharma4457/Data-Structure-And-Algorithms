#include <iostream>

using namespace std;

int main()
{
    int num = 10;
    float dec = 4.567;
    char c = 'a';
    bool is = true;
    double deci = 3.463756;
    long int numb = 464363878;

    cout << "Integer: " << num << endl;
    cout << "Decimal: " << dec << endl;
    cout << "Character: " << c << endl;
    cout << "Boolean: " << is << endl;
    cout << "Double: " << deci << endl;
    cout << "Long: " << numb << endl;

    return 0;
}